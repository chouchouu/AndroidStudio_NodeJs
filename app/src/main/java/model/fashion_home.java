package model;

public class fashion_home {
    private int img;
    private String ten;

    public fashion_home() {
    }

    public fashion_home(int img, String ten) {
        this.img = img;
        this.ten = ten;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
}
